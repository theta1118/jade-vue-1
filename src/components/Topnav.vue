<template>
  <div class="topnav">
    <div
      class="logo"
      @click="toggleMenu"
    >LOGO</div>
    <ul class="menu">
      <li>菜单1</li>
      <li>菜单2</li>
		<span class="toggleAside"></span>
    </ul>
